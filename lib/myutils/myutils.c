/*
 * Return the maximum of two double precision numbers
 */
double dmax(double a, double b)
{
  if (a >= b) return a;
  else return b;
}


/*
 * Return the minimum of two double precision numbers
 */
double dmin(double a, double b) 
{
  if (a <= b) return a;
  else return b;
}
